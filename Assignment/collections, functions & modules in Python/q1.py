# What is List? How will you reverse a list? 

# => A list in Python is a versatile data structure that allows you to store an ordered collection of items.
# => These items can be of any data type, including integers, strings, and even other lists.
# => Lists are mutable, meaning you can change their content without changing their identity.

list = [1,2,3,4,5]

list.reverse()
print(list)