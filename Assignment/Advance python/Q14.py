#  How many except statements can a try-except block have? Name Some built-in exception classes: 

'''except Statements: A try block in Python can have multiple except blocks.
                      You can catch different types of exceptions by specifying them individually.
                      Generally, you’d only include the necessary except blocks relevant to your specific error-handling needs.'''


'''Built-in Exception Classes:


ZeroDivisionError: Raised when you attempt to divide by zero.

ValueError: Occurs when a function receives an argument of the correct type but with an inappropriate value.

TypeError: Raised when an operation is performed on incompatible data types.

IndexError: Happens when you try to access an index that doesn’t exist in a sequence (e.g., list, tuple).

NameError: If you use a variable or function name that hasn’t been defined.

FileNotFoundError: Raised when a file is not found during file I/O operations.

KeyError: Occurs when you try to access a dictionary key that doesn’t exist.

AssertionError: Used for debugging; raised when an assert statement fails.

OverflowError: Raised when an arithmetic operation exceeds the limits of the data type.

ImportError: If an import statement fails to find the specified module.'''